void GangLand::displayBoard(){
    
    for (int y = 0; y < 5; y++) {
        
        for (int x = 0; x < 5; x++) {
            
            // display user's location
            if (getRowLocation() == y && getColLocation() == x ) {
                
                cout << " X ";
            }
            else{
                
                cout << "*";
            }
            
        } // x
    
        cout << endl;
        
    } // y
    
 
    
    
}

